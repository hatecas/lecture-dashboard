// 강의 PPT outline 생성 봇.
// 슬라이드별 outline + 발표 멘트 초안. 무료강의 실제 흐름(아이스브레이킹/썰/공감대 형성 포함)을 그대로 모사.

import { loadPlannerConfig } from './_config'
import { callPlannerLLM, buildContextMessage } from './_anthropic'

const FEATURE_KEY = 'ppt'

const DEFAULT_REFERENCES = `=== 좋은 무료강의 PPT 구조 예시 (실제 진행 순서) ===
0) 입장 환영 + 워밍업 — "어디서 오셨어요?", "박수 한 번 부탁드려요" (2~4장)
1) 강사 자기소개 + 라포 형성 — 사진/일상/취미 곁들인 친근한 자기소개 (3~5장)
2) 오늘 약속 — "이거 들으면 뭐가 달라지는지" 명확히 (2~3장)
3) 아이스 브레이킹 / 강사 개인 썰 — 실패담, 우연한 발견, 감정적 일화 (3~5장)
4) 시장 통념 깨기 — "다들 ~한다고 알지만 실제로는…" (3~5장)
5) 강사 본인 시행착오 — 구체적 수치/스크린샷/캡쳐 (5~8장)
6) 인사이트 정리 — 1~3 단계 프레임워크, 각 단계 사례 곁들임 (8~12장)
7) 적용 사례 / Q&A 빌드업 — Before/After, 수강생 후기 (4~6장)
8) 한계 + 솔루션 안내 — "혼자 하면 어려운 부분이 있어요" (3~4장)
9) 정규 강의 안내 — 커리큘럼, 가격, 혜택, 후기 (4~6장)
10) 마무리 + Q&A — 감사 인사, 마지막 멘트 (2~3장)

=== 강사 개인 썰/넋두리도 PPT에 들어가야 한다 ===
- "정보 슬라이드"만으로는 무료강의가 절대 안 됨. 분위기·라포·감정 빌드업이 60% 이상.
- 강사가 영상에서 "사실 제가 처음에…" 같은 개인 일화를 풀면 그것도 한 슬라이드.
- "잠깐, 이거 보세요" 같은 전환 슬라이드, 박수 유도, 채팅창 반응 유도도 슬라이드.
- 슬라이드 내용이 짧아도 OK. 한 줄짜리 슬라이드, 빈 슬라이드(영상/이미지 띄우는 용도)도 정상.

=== 발표 멘트 톤 예시 ===
"여러분, 솔직히 말씀드리면 이건 제가 1년 전에는 절대 몰랐던 거예요.
지금부터 보여드리는 화면 — 이게 제가 처음으로 월 100을 넘긴 그달의 정산 캡처예요…"`

const DEFAULT_INSTRUCTIONS = `## 분량 — 매우 중요
- 무료강의는 보통 90~120분, 슬라이드 **40~80장**이 표준. 강사 자료가 길면 60~100장까지.
- 절대 18~25장 같은 "요약본"을 만들지 말 것. **실제 강의에서 넘기는 슬라이드 한 장 한 장을 다 만들어라.**
- 분량은 강사 자료(녹음/메모/레퍼런스 PPT)의 흐름을 모두 커버하도록.

## 포함해야 할 슬라이드 종류 — 정보만 X
1. **정보/인사이트 슬라이드** — 핵심 메시지, 프레임워크, 수치, 사례
2. **아이스 브레이킹 슬라이드** — 강사 자기소개, 일상 사진, 취미, 라포 형성용
3. **개인 썰/일화 슬라이드** — "사실 제가…", "한번은 이런 일이 있었는데…" 류 감정 빌드업
4. **워밍업/전환 슬라이드** — "박수 한 번", "채팅에 ○○ 쳐주세요", "잠깐 쉬어가요"
5. **공감대/넋두리 슬라이드** — 강사가 무대 위에서 "이게 사실 쉽지 않거든요" 식으로 풀어내는 분위기 형성
6. **빈/이미지 슬라이드** — 영상 재생, 이미지 띄우기, Q&A 띄우기 등 본문은 거의 없고 발표 멘트만

→ 6가지가 다 섞여야 진짜 무료강의. 1번만 가득하면 광고/세미나 자료처럼 됨.

## 각 슬라이드 작성 규칙
- 슬라이드 제목: 짧게(20자 이내). 아이스브레이킹/썰 슬라이드는 한 줄 메시지나 의문문 형태도 OK.
- 본문 불릿: 0~5개. 빈 슬라이드는 빈 배열로. 본문은 키워드/수치 위주.
- 발표 멘트(speakerNotes): 강사가 1인칭으로 말하듯 자연스럽게. **80~400자**.
  - 정보 슬라이드는 짧고 명확하게, 썰 슬라이드는 길고 감정적으로.
- 마지막 4~6장은 정규 강의 모집 자연스러운 전환 + 마무리.

## 강사 자료 활용
- 첨부된 강사 녹음/메모/레퍼런스 PPT의 흐름을 **그대로 따라가라**.
- 강사가 영상에서 1분 동안 자기 얘기를 했으면, 그것도 PPT 1~2장으로 만들어 발표 멘트에 풀어 써라.
- 레퍼런스 PPT에 슬라이드 200장이 있으면 그것에 비례하는 분량으로.`

function buildSystemPrompt({ references, instructions }) {
  return `당신은 N잡연구소의 강의 PPT 기획 전문가입니다. 무료강의 PPT의 슬라이드별 outline과 발표 멘트 초안을 작성합니다.

당신이 만드는 것은 "요약본"이 아니라 **실제 강의에서 강사가 한 장씩 넘기는 진짜 슬라이드 시퀀스**입니다. 정보 슬라이드뿐 아니라 아이스 브레이킹, 강사 개인 썰, 공감대 형성, 워밍업 슬라이드까지 모두 포함합니다.

== 참고 모범 사례 ==
${references}

== 작성 지침 ==
${instructions}

== 출력 형식 ==
반드시 아래 JSON 구조 하나만 출력. 마크다운 코드블록 X.
{
  "title": "강의 전체 제목",
  "totalSlides": 60,
  "slides": [
    {
      "slideNumber": 1,
      "title": "슬라이드 제목 (또는 한 줄 메시지)",
      "kind": "intro|icebreaker|story|info|transition|warmup|empathy|cta|outro",
      "bullets": ["불릿 1", "불릿 2"],
      "speakerNotes": "발표 멘트 80~400자"
    }
  ]
}

kind 값:
- intro: 강사 자기소개·오늘 약속
- icebreaker: 아이스 브레이킹 (인사·박수·간단 질문)
- story: 강사 개인 일화·시행착오·썰
- info: 정보·프레임워크·사례·수치
- transition: 전환·휴식·"잠깐만요" 류
- warmup: 채팅창 반응 유도·박수 유도
- empathy: 공감대 형성·넋두리
- cta: 정규 강의 안내·모집
- outro: 마무리·감사·Q&A`
}

export async function planPpt(ctx) {
  const { instructions, references, source } = await loadPlannerConfig(FEATURE_KEY, {
    instructions: DEFAULT_INSTRUCTIONS,
    references: DEFAULT_REFERENCES,
  })
  const systemPrompt = buildSystemPrompt({ instructions, references })
  const userMessage = `다음 강의의 PPT outline + 발표 멘트를 작성하세요.

${buildContextMessage(ctx)}

요구사항:
- 강사 자료(첨부/레퍼런스/녹음)의 흐름을 그대로 따라가며 **실제 진행 슬라이드 한 장씩** 만들어라.
- 정보 슬라이드뿐 아니라 아이스 브레이킹·강사 개인 썰·공감대 형성·워밍업 슬라이드도 포함.
- "요약본"이 아니라 진짜 무료강의 슬라이드 시퀀스. 보통 40~80장.
- 위 JSON 구조 그대로 출력. 마크다운 코드블록 절대 X.`
  const result = await callPlannerLLM({ systemPrompt, userMessage, maxTokens: 16000 })
  return { ...result, configSource: source }
}
