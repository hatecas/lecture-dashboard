function buildSystemPrompt({ references, instructions }) {
  return `당신은 N잡연구소 단톡방 운영 전문가입니다. 무료강의 단톡방 신규 입장자가 처음 보는 [입장시 필독] 공지 한 개를 작성합니다.

== 표준 양식 (반드시 이 양식을 따라가세요) ==
${references}

== 작성 지침 ==
${instructions}

== 출력 형식 ==
반드시 아래 JSON 구조 하나만 출력. 마크다운 코드블록 X. 추가 설명 X.
{
  "fullText": "위 표준 양식의 #{...}를 채운 단톡방 공지 전체 본문 한 덩어리. 채팅방에 그대로 복붙 가능한 형태.",
  "instructorName": "강사 활동명",
  "instructorDescription": "#{강사 설명}에 들어간 1~2문장",
  "hooks": ["✔ 후킹1", "✔ 후킹2", "✔ 후킹3"],
  "freeClassDate": "#{무료강의 일시}에 들어간 값 또는 '[강사 확인 필요]'",
  "ebookHook": "#{전자책 후킹 카피}에 들어간 값",
  "placeholders": ["채워지지 않은 #{...} 자리표시자 목록 (예: ['#{구글 링크}', '#{드라이브 링크}'])"]
}`
}

export async function planGroupAnnouncement(ctx) {
  const { instructions, references, source } = await loadPlannerConfig(FEATURE_KEY, {
    instructions: DEFAULT_INSTRUCTIONS,
    references: DEFAULT_REFERENCES,
  })
  const systemPrompt = buildSystemPrompt({ references, instructions })
  const userMessage = `다음 강의의 단톡방 입장시 필독 공지를 위 표준 양식에 맞춰 작성하세요.\n\n${buildContextMessage(ctx)}\n\nJSON만 출력. 자료에 없는 정보는 #{...} 자리표시자 그대로 두세요.`
  const result = await callPlannerLLM({ systemPrompt, userMessage, maxTokens: 4000 })
  return { ...result, configSource: source }
}
